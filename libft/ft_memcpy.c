#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char		*pdst;
	const char	*psrc;

	pdst = dst;
	psrc = src;
	if (!dst && !src)
		return (0);
	while (n-- > 0)
		*pdst++ = *psrc++;
	return (dst);
}
